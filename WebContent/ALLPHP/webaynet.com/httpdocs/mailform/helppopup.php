<b>How to create a form</b><br><br>
Form Maker Pro allows you to create simple forms as well as forms containing several pages and featured with preview page.
<br><br>
<u>How to create a simple one page form</u><br><br>
1) Click the "Add new page" button and follow the wizard instructions to generate html page for your form.When html code is generated you will be redirected back to this panel.<br>
2) If you need to have form data sent by email or just need to set up an autoresponder, click "Add email template" button to create email template for your form. To send several different emails from one form simply create several email templates each with unique configuration you need. <br>
3) If you need to store form data in the MySQL database, click "Add log template" button to create log template for your form. To store different data in several different tables simply create several log templates. <br>
4) Your form will also require error template - the page that will be shown if for example user left some of required fields in the form blank. The error template is generated automatically by Form Maker Pro, however if you are familiar with HTML you can edit it to look and feel more like the rest of your site. <br>
5) Click "Get pages" button to download the zip archive with files package needed for correct work of your web form . Extract the zip archive to your computer and then simply upload these  files to your server following the "readme" file instructions (You will find this file in the downloaded zip archive).  <br>
<br><br>
<u>How to create a multiple pages form</u><br><br>
1) Add required amount of html pages. To add each new page click the "Add new page" button, and follow the wizard instructions. To arrange pages in the order you need, please set the "position" property for each page while adding it to the form. <br>
2) Generate email templates and/or log templates in the same way as for simple one page form and download your pages. <br>




